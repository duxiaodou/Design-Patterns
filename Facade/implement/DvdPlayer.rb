class DvdPlayer

  def on
    puts 'Hello, DvdPalyer is running'
  end

  def off
    puts 'Goodbye, DvdPalyer is closeing'
  end

  def push
    puts 'Please wait a moment'
  end

  def eject
    puts 'Please insert DVD'
  end

  def play
    puts 'play...... play...... play......'
  end

  def pause
    puts 'pause...... pause...... pause......'
  end

  def stop
    puts 'DvdPalyer have been suspended'
  end
end